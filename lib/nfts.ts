export type Category = "apparel" | "land" | "packs" | "ships" | "weapons" | "other";
export type Network = "Cardano" | "Base";
export type RarityTier = "common" | "uncommon" | "rare" | "legendary" | "mythic";
export type ReadMoreKey = "customDome" | "lands" | "fileNodes";


 
export type RarityEntry = {
  tier: RarityTier;
  label: string;        // اسم الندرة/العنصر
  percent?: number;     // نسبة من الإمداد (0-100)
  total?: number;       // عدد القطع
  image?: string;       // مسار صورة صغيرة
};

export type NFTItem = {


   // ... الحقول الموجودة
  readMorePath?: string;
  readMoreKey?: ReadMoreKey;
  // أساسي
  slug: string; // e.g. "burger-barn-deluxe-1"
  category: Category;
  name: { ar: string; en: string };
  image: string; // مثال: "/images/nfts/apparel/burger-barn-deluxe-1.png"

  // معلومات
  description?: { ar: string; en: string };
  utility?: { ar: string; en: string };
  mintDate?: string;        // ISO أو نص بشري
  mintPrice?: string;       // مثال: "50 ADA" أو "5.00$"
  networks: Network[];

  // الإمداد والندرات - موحّدين لكل العناصر
  totalSupply?: number;     // ضيف الرقم لما يتوفّر
  rarities: RarityEntry[];  // خليه [] لو لسه

  // معرّفات الشبكات (اختياري)
  contract?: string;        // Base
  policyId?: string;        // Cardano
  openseaSlug?: string;     // بديل عن العقد لجلب بيانات OpenSea

  // روابط (اختياري)
  links?: {
    mint?: string;
    market?: string;        // متجر عام واحد (لو موجود)
    marketBase?: string;    // OpenSea (Base)
    marketCardano?: string; // jpg.store (Cardano)

  };
};

