// (اختياري) فحص جمع النسب/المجاميع
export function validateRarities(nft: NFTItem) {
  if (!nft.totalSupply || !nft.rarities?.length) return;
  const sumTotals = nft.rarities.reduce((s, r) => s + (r.total || 0), 0);
  const sumPerc   = nft.rarities.reduce((s, r) => s + (r.percent || 0), 0);
  if (sumTotals && sumTotals !== nft.totalSupply) {
    console.warn(`[${nft.slug}] totals (${sumTotals}) ≠ totalSupply (${nft.totalSupply})`);
  }
  if (sumPerc && Math.round(sumPerc) !== 100) {
    console.warn(`[${nft.slug}] percents (${sumPerc}) ≠ 100`);
  }
}

