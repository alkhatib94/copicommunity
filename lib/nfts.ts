export function listByCategory(cat: Category): NFTItem[] {
  return nfts.filter((n) => n.category === cat);
}

export function getNFTBySlug(cat: Category, slug: string): NFTItem | null {
  return nfts.find((n) => n.category === cat && n.slug === slug) || null;
}

